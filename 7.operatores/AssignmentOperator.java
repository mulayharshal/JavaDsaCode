public class AssignmentOperator {
    public static void main(String[] args) {
        
        int a= 10;
        System.out.println(a+=5);
        int b= 10;
        System.out.println(b-=5);
        int c= 10;
        System.out.println(c*=5);
        int d= 10;
        System.out.println(d/=5);
        int e= 10;
        System.out.println(e%=5);
        int g =10;
        System.out.println(g=11);
    }
    
}
