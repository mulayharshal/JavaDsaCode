import java.util.*;

public class Input {
    public static void main(String[] args) {
        Scanner sc= new Scanner(System.in);

        int a = sc.nextInt();
        System.out.println(a);

        String name = sc.next();
         System.out.println(name);

        Float no = sc.nextFloat();
         System.out.println(no);
        
        double n = sc.nextDouble();
         System.out.println(n);

        
    }
    
}
