public class SquarePattern {
    public static void main (String [] args){
        //print the square pattern using loops

        //for loop
        for (int i=0;i<=4; i++){
            System.out.println("****");
        }

        System.out.println("");
        //while loop 
        int l= 0;
        while(l<5){
            System.out.println("****");
            l++;
        }
    }
    
}
